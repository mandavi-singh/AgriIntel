def get_yield_history(country, crop):
    country_data = YIELD_HISTORY.get(country, {})
    crop_data    = country_data.get(crop, {})
    if not crop_data:
        return None
    df = pd.DataFrame(list(crop_data.items()), columns=["Year", "Yield"])
    return df

def get_available_crops_history(country):
    return list(YIELD_HISTORY.get(country, {}).keys())
